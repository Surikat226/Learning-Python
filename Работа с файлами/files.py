# Способ работы  номер 1 (наименее безопасный)
f = open("src_files/Звезда по имени Солнце.txt", "rt", encoding="utf-8") # указали энкодинг utf-8, т.к. у нас Кириллица, иначе будут крокозябры
content = f.read() # считываем файл и присваиваем этот метод переменной
print(content) # выводим считанный контент
f.close()

print("---------------")


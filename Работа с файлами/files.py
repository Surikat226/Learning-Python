# Способ работы номер 3 (наиболее универсальный) - автоматом закрывает файл
lines = ["Это", "рандомный", "текст", "для", "записи"]
with open("src_files/Сгенеренный Питоном файл.txt", "w") as f_3:
    for line in lines:
        f_3.write(line + "\n")


